// Función para obtener un array plano de todas las categorías
const getAllCategories = () => {
    return Object.entries(productCategories).reduce((acc, [main, sub]) => {
        return [...acc, ...sub.map(item => ({
            main,
            sub: item,
            value: item.toLowerCase().replace(/ /g, '_')
        }))];
    }, []);
};

// Función para obtener las categorías principales
const getMainCategories = () => {
    return Object.keys(productCategories);
};

// Función para obtener las subcategorías de una categoría principal
const getSubCategories = (mainCategory) => {
    return productCategories[mainCategory] || [];
};

// Función para validar si una categoría existe
const isValidCategory = (category) => {
    const allCategories = getAllCategories();
    return allCategories.some(cat => cat.value === category);
};

module.exports = {
    productCategories,
    getAllCategories,
    getMainCategories,
    getSubCategories,
    isValidCategory
}; 